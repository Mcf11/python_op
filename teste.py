word = "ABCDEFGHIJK"
print(word[0])    #pega uma letra só
print(word[2:])   #do index 2 pra frente
print(word[:6])   #até o index6 (não incluindo ele)
print(word[2:6])  #do index 2 ao 6
print(word[1:3])  #inclui o index 1 e vai até o index 2, parando um index antes do informado
print(word[::2])  #all the way from beginning to the end jumping from 2 to 2

